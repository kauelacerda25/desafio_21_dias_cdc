# Dia 12: Estudo de Git + primeiro repositório público

# Este é um exemplo simples de como usar o Git para controle de versão.
# Comandos básicos do Git:
# git init - Inicializa um novo repositório Git
# git add . - Adiciona todos os arquivos ao staging area
# git commit -m "Mensagem do commit" - Cria um commit com uma mensagem descritiva
# git remote add origin <URL do repositório> - Adiciona um repositório remoto
# git push - Envia os commits locais para o repositório remoto 
# git pull - Puxa as últimas mudanças do repositório remoto para o local
# git status - Verifica o status dos arquivos no repositório
# git log - Mostra o histórico de commits
# Este arquivo dia12.py foi criado para ser adicionado ao repositório público.